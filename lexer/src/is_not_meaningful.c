/*
** meaningful characters: ; | & > < ! " ' space
*/
int		is_not_meaningful(char c)
{
	return (c != ';' && c != '|' && c != '&' && c != '!'
		&& c != '>' && c != '<' && c != '\'' && c != '"'
		&& c != ' ' && c != '\t' && c != '\v' && c != '\f'
		&& c != '\r' && c != '\n');
}
