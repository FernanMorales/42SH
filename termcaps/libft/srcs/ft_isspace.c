#include <string.h>

int		ft_isspace(int c)
{
	size_t	cp;

	cp = (size_t)c;
	if (cp == ' ' || cp == '\n' || cp == '\t' || cp == '\v' || cp == '\r'
			|| cp == '\f')
		return (1);
	else
		return (0);
}
