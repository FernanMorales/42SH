#include "sh42.h"

void	ms_environ_print(char **env)
{
	size_t		i;

	i = 0;
	while (env[i])
	{
		write(1, env[i], ckstd_strlen(env[i]));
		write(1, "\n", 1);
		i++;
	}
}
